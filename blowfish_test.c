#include "blowfish.h"

#define TESTCASES 16

typedef struct
{
    BLOWFISH_UINT8 key[8];
    BLOWFISH_UINT8 plainText[8];
    BLOWFISH_UINT8 cipher[8];
} Blowfish_EncryptTestCase;

BLOWFISH_UINT8 Equal(const BLOWFISH_UINT8 *dst, const BLOWFISH_UINT8 *src)
{
    for (BLOWFISH_UINT8 i = 0; i < 8; i++)
    {
        if (dst[i] != src[i])
        {
            return 0;
        }
    }
    return 1;
}

